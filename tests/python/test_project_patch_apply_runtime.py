def test_cli_dry_run_works(tmp_path: Path, monkeypatch: pytest.MonkeyPatch, capsys: pytest.CaptureFixture[str]) -> None:
    _allow_temp(monkeypatch)
    root = _project_root(tmp_path)
    patch = _patch_file(root)

    exit_code = patch_apply_main(["--project-root", str(root), "--patch", str(patch), "--dry-run"])

    payload = json.loads(capsys.readouterr().out)
    assert exit_code == 0
    assert payload["status"] == "DRY_RUN"
