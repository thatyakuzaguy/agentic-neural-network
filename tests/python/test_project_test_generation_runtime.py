def test_generated_patch_requires_approval_to_apply(tmp_path: Path, monkeypatch: pytest.MonkeyPatch) -> None:
    _allow_temp(monkeypatch)
    root = _project(tmp_path)
    generated = generate_project_tests(root, "outputs/runs/test-generation")

    dry = apply_project_patch(root, generated.test_patch_files[0], dry_run=True)
    blocked = apply_project_patch(root, generated.test_patch_files[0], dry_run=False)

    assert dry.status == "DRY_RUN"
    assert blocked.status == "BLOCKED"
    assert not (root / "tests" / "python" / "test_project_contract.py").exists()


def test_generated_patch_can_be_applied_and_verified(tmp_path: Path, monkeypatch: pytest.MonkeyPatch) -> None:
    _allow_temp(monkeypatch)
    root = _project(tmp_path)
    generated = generate_project_tests(root, "outputs/runs/test-generation")

    applied = apply_project_patch(
        root,
        generated.test_patch_files[0],
        approval_token="local-test-token",
        confirm_apply=True,
        dry_run=False,
    )
    verified = run_project_verification(root, confirm_run=True)

    assert applied.status == "APPLIED"
    assert verified.status == "PASSED"
    assert verified.commands_executed


def test_cli_run_works(tmp_path: Path, monkeypatch: pytest.MonkeyPatch, capsys: pytest.CaptureFixture[str]) -> None:
    _allow_temp(monkeypatch)
    root = _project(tmp_path)

    exit_code = generation_cli_main(
        [
            "--project-root",
            str(root),
            "--run-dir",
            "outputs/runs/test-generation",
            "--max-tests",
            "2",
        ]
    )

    output = capsys.readouterr().out
    assert exit_code == 0
    assert '"status": "VALID"' in output
