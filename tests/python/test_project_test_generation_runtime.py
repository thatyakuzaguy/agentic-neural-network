def _allow_temp(monkeypatch: pytest.MonkeyPatch) -> None:
    monkeypatch.setenv("ANN_ALLOW_TEMP_PROJECT_TEST_GENERATION_TARGETS", "1")
    monkeypatch.setenv("ANN_ALLOW_TEMP_PROJECT_PATCH_TARGETS", "1")
    monkeypatch.setenv("ANN_ALLOW_TEMP_PROJECT_TEST_TARGETS", "1")
    monkeypatch.setenv("ANN_PROJECT_PATCH_TOKEN", "local-test-token")


def _project(tmp_path: Path) -> Path:
    root = tmp_path / "demo-project"
    root.mkdir()
    (root / "README.md").write_text(
        "# Demo Project\n\nPurpose: generated by ANN for local verification.\n",
        encoding="utf-8",
    )
    return root


def test_generates_67_plan_artifacts(tmp_path: Path, monkeypatch: pytest.MonkeyPatch) -> None:
    _allow_temp(monkeypatch)
    root = _project(tmp_path)

    result = generate_project_tests(root, "outputs/runs/test-generation")

