def _project_root(tmp_path: Path) -> Path:
    root = tmp_path / "generated_project"
    (root / "apps" / "api" / "app").mkdir(parents=True)
    (root / "outputs" / "runs" / "20260621_120000" / "patches").mkdir(parents=True)
    (root / "apps" / "api" / "app" / "main.py").write_text('print("old")\n', encoding="utf-8")
    return root


def _patch_file(root: Path, *, relative: str = "docs/features/accounts.md", body: str = "Accounts") -> Path:
    patch = root / "outputs" / "runs" / "20260621_120000" / "patches" / "patch_001.diff"
    patch.write_text(
        "\n".join(
            [
                f"diff --git a/{relative} b/{relative}",
                "new file mode 100644",
                "index 0000000..1111111",
                "--- /dev/null",
                f"+++ b/{relative}",
                "@@ -0,0 +1,3 @@",
                f"+# {body}",
                "+",
                "+Generated by test.",
                "",
            ]
        ),
        encoding="utf-8",
    )
    return patch


def _existing_file_patch(root: Path) -> Path:
    patch = root / "outputs" / "runs" / "20260621_120000" / "patches" / "patch_existing.diff"
    patch.write_text(
        "\n".join(
            [
                "diff --git a/apps/api/app/main.py b/apps/api/app/main.py",
                "--- a/apps/api/app/main.py",
                "+++ b/apps/api/app/main.py",
                "@@ -1 +1 @@",
                '-print("old")',
                '+print("new")',
                "",
            ]
        ),
        encoding="utf-8",
    )
    return patch


def _allow_temp(monkeypatch: pytest.MonkeyPatch) -> None:
    monkeypatch.setenv("ANN_ALLOW_TEMP_PROJECT_PATCH_TARGETS", "1")


def test_dry_run_no_modifies_files(tmp_path: Path, monkeypatch: pytest.MonkeyPatch) -> None:
    _allow_temp(monkeypatch)
    root = _project_root(tmp_path)
    patch = _patch_file(root)

    result = apply_project_patch(root, patch, dry_run=True)

    assert result.status == "DRY_RUN"
    assert result.files_modified == []
    assert not (root / "docs" / "features" / "accounts.md").exists()


